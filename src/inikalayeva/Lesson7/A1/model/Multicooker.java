package inikalayeva.Lesson7.A1.model;

public class Multicooker extends Cooking{
    private int volumeOfBowl;
    private String innerBowlCover;
    private String shapeOfBowl;

    public int getVolumeOfBowl() {
        return volumeOfBowl;
    }

    public void setVolumeOfBowl(int volumeOfBowl) {
        this.volumeOfBowl = volumeOfBowl;
    }

    public String getInnerBowlCover() {
        return innerBowlCover;
    }

    public void setInnerBowlCover(String innerBowlCover) {
        this.innerBowlCover = innerBowlCover;
    }

    public String getShapeOfBowl() {
        return shapeOfBowl;
    }

    public void setShapeOfBowl(String shapeOfBowl) {
        this.shapeOfBowl = shapeOfBowl;
    }
}
